// Build-time fetch of a LeetCode user's public submission calendar.
//
// Runs as the `prebuild` step (see package.json) so the data is baked into the
// bundle as a static TS module — the visitor's browser never calls LeetCode
// (which sends no CORS headers and would block a direct browser request).
//
// Resilient by design: if the network call fails it keeps any existing data
// file and exits 0, so a transient LeetCode outage never breaks a deploy.

import { writeFile, readFile, mkdir } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const USERNAME = process.env.LEETCODE_USERNAME || 'rasco1999';
const ENDPOINT = 'https://leetcode.com/graphql/';
const OUT_FILE = resolve(
  dirname(fileURLToPath(import.meta.url)),
  '../src/data/leetcode.ts'
);

const CALENDAR_QUERY = `
  query userProfileCalendar($username: String!, $year: Int) {
    matchedUser(username: $username) {
      userCalendar(year: $year) {
        activeYears
        streak
        totalActiveDays
        submissionCalendar
      }
    }
  }
`;

async function gql(variables) {
  const res = await fetch(ENDPOINT, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      Referer: 'https://leetcode.com',
      'User-Agent':
        'Mozilla/5.0 (portfolio build script; +https://oscarjones.space)',
    },
    body: JSON.stringify({ query: CALENDAR_QUERY, variables }),
  });
  if (!res.ok) throw new Error(`LeetCode HTTP ${res.status}`);
  const json = await res.json();
  if (json.errors?.length) {
    throw new Error(json.errors.map((e) => e.message).join('; '));
  }
  const cal = json.data?.matchedUser?.userCalendar;
  if (!cal) throw new Error(`No calendar for user "${USERNAME}"`);
  return cal;
}

function renderModule({ submissionCalendar, totalActiveDays, streak }) {
  const data = {
    username: USERNAME,
    fetchedAt: new Date().toISOString(),
    totalActiveDays,
    streak,
    submissionCalendar,
  };
  return `// AUTO-GENERATED by scripts/fetch-leetcode.mjs — do not edit by hand.
// Regenerate with: npm run fetch:leetcode

export interface LeetCodeActivity {
  username: string;
  /** ISO timestamp of when this snapshot was fetched at build time. */
  fetchedAt: string;
  totalActiveDays: number;
  streak: number;
  /** Map of UTC-midnight unix-second (as a string) -> submissions that day. */
  submissionCalendar: Record<string, number>;
}

export const leetcodeActivity: LeetCodeActivity = ${JSON.stringify(
    data,
    null,
    2
  )};

export default leetcodeActivity;
`;
}

async function main() {
  try {
    // One query per active year, merged into a single calendar so the rolling
    // 12-month window always has both the current and previous year covered.
    const seed = await gql({ username: USERNAME });
    const years =
      seed.activeYears?.length > 0
        ? seed.activeYears
        : [new Date().getUTCFullYear()];

    const merged = {};
    let totalActiveDays = seed.totalActiveDays ?? 0;
    let streak = seed.streak ?? 0;

    for (const year of years) {
      const cal = await gql({ username: USERNAME, year });
      const map = JSON.parse(cal.submissionCalendar || '{}');
      for (const [ts, count] of Object.entries(map)) {
        merged[ts] = (merged[ts] || 0) + Number(count);
      }
      // The unscoped seed query already carries the freshest streak/totals.
    }

    await mkdir(dirname(OUT_FILE), { recursive: true });
    await writeFile(
      OUT_FILE,
      renderModule({ submissionCalendar: merged, totalActiveDays, streak }),
      'utf8'
    );
    const days = Object.keys(merged).length;
    console.log(
      `[leetcode] wrote ${days} active days for "${USERNAME}" -> src/data/leetcode.ts`
    );
  } catch (err) {
    console.warn(`[leetcode] fetch failed: ${err.message}`);
    // Keep an existing snapshot if we have one; otherwise write a valid empty
    // module so the build (and the import in the component) never breaks.
    try {
      await readFile(OUT_FILE, 'utf8');
      console.warn('[leetcode] keeping existing src/data/leetcode.ts');
    } catch {
      await mkdir(dirname(OUT_FILE), { recursive: true });
      await writeFile(
        OUT_FILE,
        renderModule({ submissionCalendar: {}, totalActiveDays: 0, streak: 0 }),
        'utf8'
      );
      console.warn('[leetcode] wrote empty fallback src/data/leetcode.ts');
    }
  }
}

main();
